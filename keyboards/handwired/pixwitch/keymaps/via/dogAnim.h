// Setup some mask which can be or'd with bytes to turn off pixels
const uint8_t single_bit_masks_out[8] = {127, 191, 223, 239, 247, 251, 253, 254};

static void fade_out_display(void) {
    // Define the reader structure
    oled_buffer_reader_t reader;
    uint8_t              buff_char;
    if (random() % 15 == 0) {
        srand(timer_read());
        // Fetch a pointer for the buffer byte at index 0. The return structure
        // will have the pointer and the number of bytes remaining from this
        // index position if we want to perform a sequential read by
        // incrementing the buffer pointer
        reader = oled_read_raw(0);
        // Loop over the remaining buffer and erase pixels as we go
        for (uint16_t i = 0; i < reader.remaining_element_count; i++) {
            // Get the actual byte in the buffer by dereferencing the pointer
            buff_char = *reader.current_element;
            if (buff_char != 0) {
                oled_write_raw_byte(buff_char & single_bit_masks_out[rand() % 8], i);
            }
            // increment the pointer to fetch a new byte during the next loop
            reader.current_element++;
        }
    }
}

// INIT A BUNCH OF VARS
static uint8_t  bytes_drawn[512] = {0};
static uint16_t drawn_count      = 0;
static uint32_t start_time       = 0;
static uint32_t total_duration   = 500;  // Duration in milliseconds to draw the logo
static uint32_t hold_duration    = 2000; // Duration to hold the completed logo
static uint32_t draw_interval    = 0;
static bool     draw_complete    = false;
static bool     holding_logo     = false;
static uint32_t hold_start_time  = 0;
static bool     logo_held        = false;

static uint8_t  frame_rate_ms = 100;
static uint8_t  frame         = 0;
static uint32_t anim_timer    = 0;
static bool     first_start   = true;
static int16_t  pan_offset    = 0;
static uint32_t sleep_timeout = 0;
static bool     sleeping      = false;
static bool     going_to_bed  = false;

static bool roll_left  = false;
static bool roll_right = false;

#define FRAME_WIDTH 168
#define FRAME_HEIGHT 32
#define FRAME_SIZE (FRAME_WIDTH * FRAME_HEIGHT / 8)
#define SCREEN_WIDTH 128
#define SCREEN_HEIGHT 32
#define SCREEN_SIZE (SCREEN_WIDTH * SCREEN_HEIGHT / 8)

static void draw_logo_randomly(void) {
    if (draw_complete) {
        return;
    }

    if (drawn_count >= sizeof(logo[0])) {
        draw_complete   = true;
        hold_start_time = timer_read32();
        return;
    }

    uint16_t logo_center = sizeof(logo[0]) / 2;
    uint16_t distance_from_center;
    uint16_t index;

    do {
        index                = rand() % sizeof(logo[0]);
        distance_from_center = abs((int16_t)index - (int16_t)logo_center);
    } while (bytes_drawn[index] || distance_from_center > drawn_count);

    uint8_t logo_byte = pgm_read_byte(&logo[0][index]);
    oled_write_raw_byte(logo_byte, index);

    bytes_drawn[index] = 1;
    drawn_count++;
}

// BEGIN THE WACKY SHIT

void pan_left(void) {
    going_to_bed = false;
    sleeping     = false;
    if (pan_offset != 0) {
        pan_offset = (pan_offset - 1);
    } else {
        pan_offset = 0;
    }
}

void pan_right(void) {
    going_to_bed = false;
    sleeping     = false;
    if (pan_offset <= (FRAME_WIDTH - SCREEN_WIDTH)) {
        pan_offset = (pan_offset + 1);
    } else {
        pan_offset = (FRAME_WIDTH - SCREEN_WIDTH);
    }
}

void manage_dog_buffer(void) {
    for (uint8_t row = 0; row < (SCREEN_HEIGHT / 8); row++) {
        for (uint8_t col = 0; col < SCREEN_WIDTH; col++) {
            uint16_t frame_index = (row * FRAME_WIDTH) + col + pan_offset;
            uint8_t  frame_byte  = pgm_read_byte(&idle[frame][frame_index]);
            oled_write_raw_byte(frame_byte, (row * SCREEN_WIDTH) + col);
        }
    }
    frame++;
    anim_timer = timer_read32();
}

void dog_swap(void) {
    if (roll_left) {
        for (uint16_t p = 0; p < 40; p++) {
            pan_left();
        }
        roll_left = false;
        sleeping  = false;
        frame     = 5;
    } else if (roll_right) {
        for (uint16_t p = 0; p < 40; p++) {
            pan_right();
        }
        roll_right = false;
        sleeping   = false;
        frame      = 5;
    }
}

// END THE WACKY SHIT

static void render_anim(void) {
    // static bool     fading_out      = false;
    // static uint32_t fade_start_time = 0;
    oled_scroll_set_area(0, 3);
    oled_scroll_set_speed(1);

    // draws the logo if first boot
    if (first_start) {
        start_time    = timer_read32();
        draw_interval = total_duration / sizeof(logo[0]);
        draw_logo_randomly();
        first_start = false;
        // continue drawing logo if incomplete
    } else if (!draw_complete) {
        if (timer_elapsed32(start_time) >= draw_interval * drawn_count) {
            draw_logo_randomly();
        }
        return;
        // starts the timer for holding the logo on screen; delays the start of main animation
    } else if (draw_complete && !holding_logo && !logo_held) {
        hold_start_time = timer_read32();
        holding_logo    = true;
        oled_scroll_left();
    } else if (holding_logo && timer_elapsed32(hold_start_time) < hold_duration) {
        return;
        // waits for the logo hold timer to finish, then sets a flag to skip logo stuff on the next loop
    } else if (holding_logo && timer_elapsed32(hold_start_time) >= hold_duration) {
        holding_logo = false;
        logo_held    = true;
        oled_scroll_off();
        // calls the dog animation function and begins the main animation
    } else if (timer_elapsed32(anim_timer) > frame_rate_ms && frame < IDLE_FRAMES) {
        manage_dog_buffer();
        dog_swap();
    } else if (!going_to_bed && frame >= IDLE_FRAMES && timer_elapsed32(anim_timer) > frame_rate_ms) {
        sleep_timeout = timer_read32();
        going_to_bed  = true;
    } else if (!sleeping && timer_elapsed32(sleep_timeout) > 5000) {
        for (uint16_t i = 0; i < 512; i++) {
            fade_out_display();
        }
        sleeping = true;
    }
}
